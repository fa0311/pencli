export default class Default extends Command {
  static args = {
    text: Args.string({description: 'Text'}),
  }

  static description = 'Say hello'

  static examples = [`<%= config.bin %> <%= command.id %> Hello`]

  static flags = {}

  async run(): Promise<void> {
    const {args, flags} = await this.parse(Default)

    if (args.text) {
      const lines = ascii.split('\n').slice(0, -1)
      for (let i = 0; i < args.text.length; i++) {
        const pen = lines.map((line) => line.padStart(i + line.length, ' ')).join('\n')
        logUpdate(pen + '\n' + args.text.slice(0, i + 1))
        await new Promise((resolve) => setTimeout(resolve, 100))
      }
    } else {
      this.log(ascii)
    }
  }
}
